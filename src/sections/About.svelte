<div id="about">
	<h2>About</h2>

	<p>
		<span class="highly">Templates</span> allows you to search for project templates on github, bootstrapping your productivity from
		"I have to setup everything from scratch" to "This is the template I need, I can start working on my awesome idea instantly".
	</p>

	<p>
		The use case behind <span class="highly">Templates</span> is: "I want to create a desktop app using Electron and Svelte, but I have no idea how to setup everything".
		I went online and found a really good project that served as the starting point for Electron and Svelte app. But it took sometime to find it,
		so I decided to create <span class="highly">Templates</span>: a search engine for starter-kits, templates, ecc.
	</p>
